public class switch_test {
    
    public static void main(String[] args) {

        int day = 2;

        switch (day){
            case 1:
                System.out.println("mon");
                break;
            case 2:
                System.out.println("tir");
                break;
            case 3:
                System.out.println("ons");
                break;
            case 4:
                System.out.println("tor");
                break;
        }
    }
}
